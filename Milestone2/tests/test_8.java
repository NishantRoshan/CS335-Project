public class arrays {
    public static void main(String args[]){  
        int[] a=new int[5];//declaration
        char ch[] = new char[5]; 
        for (int i=0; i<a.length; i++) {  
            System.out.println(a[i]);  
        } 
        for (int i=0; i<ch.length; i++) {  
            System.out.println(ch[i]);  
        } 
    }
}
